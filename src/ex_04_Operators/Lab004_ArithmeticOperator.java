package ex_04_Operators;

public class Lab004_ArithmeticOperator {
    public static void main(String[] args) {

        int a=10;
        int b=5;

        //+,-,*,/,% are aothemtic operators
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

    }
}
